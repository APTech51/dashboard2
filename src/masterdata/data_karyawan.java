    private void ubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ubahActionPerformed
    if (id_petugas.getText().trim().isEmpty()) {

    JOptionPane.showMessageDialog(
        null,
        "Pilih data yang ingin diubah terlebih dahulu!"
    );
    return;
}       
    try {

         String sql = "UPDATE db_petugas SET nama_petugas=?, jabatan=?, no_telepon=? WHERE id_petugas=?";

        PreparedStatement ps = conn.prepareStatement(sql);

        ps.setString(1, nama_petugas.getText());
        ps.setString(2, jabatan.getSelectedItem().toString());
        ps.setString(3, no_telepon.getText());
        ps.setString(4, id_petugas.getText());

        ps.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil diubah");

        tampilData();
        kosong();
        kodeOtomatis();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
    }//GEN-LAST:event_ubahActionPerformed

