 private void cariPetugas(String id) {
        try {
            String sql = "SELECT * FROM db_petugas WHERE id_petugas = ?";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, id);
            ResultSet rs = ps.executeQuery();
 
            if (rs.next()) {
                nama_petugas.setText(rs.getString("nama_petugas"));
                jabatan.setSelectedItem(rs.getString("jabatan"));
                no_telepon.setText(rs.getString("no_telepon"));
            }
 
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
 }
 }   
        
