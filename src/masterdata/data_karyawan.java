private void tampilData() {

    DefaultTableModel model = new DefaultTableModel();

    model.addColumn("ID Petugas");
    model.addColumn("Nama Petugas");
    model.addColumn("Jabatan");
    model.addColumn("No Telepon");

    try {

        String sql = "SELECT * FROM db_petugas";

        java.sql.Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        while (rs.next()) {

            model.addRow(new Object[]{
                rs.getString("id_petugas"),
                rs.getString("nama_petugas"),
                rs.getString("jabatan"),
                rs.getString("no_telepon")
            });
        }

        jTable1.setModel(model);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
private void kodeOtomatis() {
    try {

        String sql = "SELECT id_petugas FROM db_petugas ORDER BY id_petugas DESC LIMIT 1";
        java.sql.Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

        if (rs.next()) {

            String kode = rs.getString("id_petugas");

            // contoh : PTG001
            String angka = kode.substring(3);

            int nomor = Integer.parseInt(angka) + 1;

            id_petugas.setText("PTG" + String.format("%03d", nomor));

        } else {

            id_petugas.setText("PTG001");

        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e.getMessage());
    }
}

