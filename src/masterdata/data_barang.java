    private void mencariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mencariActionPerformed
     
    String cariData = cari.getText().toLowerCase();

    for (int i = 0; i < table.getRowCount(); i++) {

        String kode   = table.getValueAt(i, 0).toString().toLowerCase();
        String kategori = table.getValueAt(i, 1).toString().toLowerCase();
        String nama   = table.getValueAt(i, 2).toString().toLowerCase();
        String ukur   = table.getValueAt(i, 3).toString().toLowerCase();
        String warnaa = table.getValueAt(i, 4).toString().toLowerCase();
        String jumlah = table.getValueAt(i, 5).toString().toLowerCase();

        if (kode.contains(cariData)
                || kategori.contains(cariData)
                || nama.contains(cariData)
                || ukur.contains(cariData)
                || warnaa.contains(cariData)
                || jumlah.contains(cariData)) {

            // Pilih baris di tabel
            table.setRowSelectionInterval(i, i);

            // Tampilkan ke form
            kd_brng.setText(table.getValueAt(i, 0).toString());
            Ktgrbrng.setSelectedItem(table.getValueAt(i, 1).toString());
            n_brng.setText(table.getValueAt(i, 2).toString());
            ukuran.setText(table.getValueAt(i, 3).toString());
            warna.setText(table.getValueAt(i, 4).toString());
            Jmlh.setText(table.getValueAt(i, 5).toString());

            // Kunci field yang tidak boleh diedit
            kd_brng.setEditable(false);
            Jmlh.setEditable(false);

            // Nama barang hanya boleh diedit kalau belum pernah transaksi
            n_brng.setEditable(bolehEditNamaBarang());

            JOptionPane.showMessageDialog(null, "Data ditemukan");
            return;
        }
    }

    JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
    }//GEN-LAST:event_mencariActionPerformed

