    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
  
    try (Connection conn = koneksi.getConnection()) {

        // Cek transaksi barang masuk
        String cekMasuk =
            "SELECT COUNT(*) FROM ts_brg_msk WHERE kd_brg = ?";

        PreparedStatement psMasuk =
            conn.prepareStatement(cekMasuk);

        psMasuk.setString(1, kd_brng.getText());

        ResultSet rsMasuk =
            psMasuk.executeQuery();

        rsMasuk.next();

        int jmlMasuk =
            rsMasuk.getInt(1);

        // Cek transaksi barang keluar
        String cekKeluar =
            "SELECT COUNT(*) FROM ts_brg_klr WHERE kd_brg = ?";

        PreparedStatement psKeluar =
            conn.prepareStatement(cekKeluar);

        psKeluar.setString(1, kd_brng.getText());

        ResultSet rsKeluar =
            psKeluar.executeQuery();

        rsKeluar.next();

        int jmlKeluar =
            rsKeluar.getInt(1);

        // Kalau pernah dipakai transaksi
        if (jmlMasuk > 0 || jmlKeluar > 0) {

            JOptionPane.showMessageDialog(
                null,
                "Barang sudah memiliki transaksi.\nTidak bisa dihapus!"
            );

            return;
        }

        // Baru hapus
        String sql =
            "DELETE FROM barang WHERE kd_brng=?";

        PreparedStatement ps =
            conn.prepareStatement(sql);

        ps.setString(1, kd_brng.getText());

        ps.executeUpdate();

        JOptionPane.showMessageDialog(
            null,
            "Data berhasil dihapus"
        );

        tampilData();
        kosong();
        kodeOtomatis();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
            null,
            e.getMessage()
        );

    }
    }//GEN-LAST:event_hapusActionPerformed

