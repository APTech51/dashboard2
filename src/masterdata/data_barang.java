    private void simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanActionPerformed
      try (Connection conn = koneksi.getConnection()) {

       
        if (n_brng.getText().trim().isEmpty()
                || ukuran.getText().trim().isEmpty()
                || warna.getText().trim().isEmpty()) {

            JOptionPane.showMessageDialog(
                null,
                "Nama Barang, Ukuran, dan Warna wajib diisi!"
            );
            return;
        }

        String sql =
            "INSERT INTO barang " +
            "(kd_brng, Ktgrbrng, n_brng, ukuran, warna, Jmlh, hg_beli, hg_jual) " +
            "VALUES (?,?,?,?,?,?,?,?)";

        PreparedStatement ps = conn.prepareStatement(sql);

        ps.setString(1, kd_brng.getText().trim());
        ps.setString(2, Ktgrbrng.getSelectedItem().toString());
        ps.setString(3, n_brng.getText().trim());
        ps.setString(4, ukuran.getText().trim());
        ps.setString(5, warna.getText().trim());

        // Stok awal dan harga awal
        ps.setInt(6, 0);
        ps.setInt(7, 0);
        ps.setInt(8, 0);

        ps.executeUpdate();

        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");

        tampilData();
        kosong();
        kodeOtomatis();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
            null,
            "Gagal menyimpan data : " + e.getMessage()
        );

    }
    
    }//GEN-LAST:event_simpanActionPerformed

