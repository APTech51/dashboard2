    protected void tampilData() {
        try (Connection conn = koneksi.getConnection()) {
           Object[] baris = {"Kode Barang", "Kategori Barang", "Nama Barang", "Ukuran", "Warna", "Jumlah"};
        tabmode = new DefaultTableModel(null, baris);
        table.setModel(tabmode);

        String sql = "SELECT * FROM barang";
        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);

        while (hasil.next()) {
            String[] data = {
                hasil.getString("kd_brng"),
                hasil.getString("Ktgrbrng"), 
                hasil.getString("n_brng"),
                hasil.getString("ukuran"),
                hasil.getString("warna"),
                hasil.getString("Jmlh")      
            };
            tabmode.addRow(data);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal tampil: " + e.getMessage());
    }
    }

    private void kodeOtomatis() {
    try (Connection conn = koneksi.getConnection()) {

        String sql =
            "SELECT MAX(CAST(SUBSTRING(kd_brng, 7) AS UNSIGNED)) AS nomor FROM barang";

        Statement st = conn.createStatement();
        ResultSet rs = st.executeQuery(sql);

       if (rs.next() && rs.getObject("nomor") != null) {
        int nomor = rs.getInt("nomor") + 1;
        kd_brng.setText(String.format("DTBRNG%02d", nomor));
        } else {
        kd_brng.setText("DTBRNG01");
        }
    } catch (Exception e) {

        JOptionPane.showMessageDialog(
            null,
            "Kode otomatis error : " + e.getMessage()
        );

    }
}
    private boolean bolehEditNamaBarang() {

        try (Connection conn = koneksi.getConnection()) {

            // Cek transaksi masuk
            String sqlMasuk =
                "SELECT COUNT(*) FROM ts_brg_msk WHERE kd_brg=?";

            PreparedStatement psMasuk =
                conn.prepareStatement(sqlMasuk);

            psMasuk.setString(1, kd_brng.getText());

            ResultSet rsMasuk =
                psMasuk.executeQuery();

            rsMasuk.next();

            int masuk = rsMasuk.getInt(1);

            // Cek transaksi keluar
            String sqlKeluar =
                "SELECT COUNT(*) FROM ts_brg_klr WHERE kd_brg=?";

            PreparedStatement psKeluar =
                conn.prepareStatement(sqlKeluar);

            psKeluar.setString(1, kd_brng.getText());

            ResultSet rsKeluar =
                psKeluar.executeQuery();

            rsKeluar.next();

            int keluar = rsKeluar.getInt(1);

            // Kalau belum pernah transaksi
            return (masuk == 0 && keluar == 0);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(
                null,
                e.getMessage()
            );

            return false;
    }
}
    
